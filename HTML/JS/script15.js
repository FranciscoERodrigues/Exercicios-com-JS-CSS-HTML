// Exercício 4
// Escreva um algoritmo que leia três números inteiros, calcule a média desses números e exiba o resultado.

var numero01 = parseInt(prompt ("Digite o primeiro valor:"))
var numero02 = parseInt(prompt ("Digite o segundo valor:"))
var numero03 = parseInt(prompt ("Digite o terceiro valor:"))

var media = (numero01 + numero02 + numero03) / 3;

alert("A média dos três números é: " + media);


